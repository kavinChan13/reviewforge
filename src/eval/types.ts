import { z } from "zod";
import { CATEGORIES, SEVERITIES } from "../report/finding.js";

export const GroundTruthSchema = z.object({
  file: z.string(),
  line: z.number().int().nonnegative(),
  endLine: z.number().int().nonnegative().optional(),
  category: z.enum(CATEGORIES).optional(),
  severity: z.enum(SEVERITIES).optional(),
  note: z.string().optional(),
});
export type GroundTruth = z.infer<typeof GroundTruthSchema>;

/** Provenance metadata for cases generated from real commits. */
export const SeedMetaSchema = z.object({
  fixCommit: z.string(),
  parentCommit: z.string(),
  source: z.string(),
});
export type SeedMeta = z.infer<typeof SeedMetaSchema>;

export const BenchCaseSchema = z.object({
  id: z.string(),
  description: z.string().default(""),
  /** Path to the case repo (after-state source), relative to the case dir. */
  repo: z.string().default("."),
  /** Diff source — exactly one of these. */
  diffFile: z.string().optional(),
  base: z.string().optional(),
  commits: z.string().optional(),
  groundTruth: z.array(GroundTruthSchema).default([]),
  labelSource: z.enum(["real", "synthetic", "negative"]).default("real"),
  /** Optional seed provenance for cases generated by scripts/seed-from-commit.ts. */
  seed: SeedMetaSchema.optional(),
});
export type BenchCase = z.infer<typeof BenchCaseSchema>;

export interface LoadedCase extends BenchCase {
  /** Absolute path to the case directory. */
  dir: string;
  /** Absolute path to the repo to review. */
  repoAbs: string;
}
